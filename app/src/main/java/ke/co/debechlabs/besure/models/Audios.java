public class Audios {
    private String audio_title, audio_link, audio_source, audio_date, audio_station_url;

    public String getAudio_title() {
        return audio_title;
    }

    public void setAudio_title(String audio_title) {
        this.audio_title = audio_title;
    }

    public String getAudio_link() {
        return audio_link;
    }

    public void setAudio_link(String audio_link) {
        this.audio_link = audio_link;
    }

    public String getAudio_source() {
        return audio_source;
    }

    public void setAudio_source(String audio_source) {
        this.audio_source = audio_source;
    }

    public String getAudio_date() {
        return audio_date;
    }

    public void setAudio_date(String audio_date) {
        this.audio_date = audio_date;
    }

    public String getAudio_station_url() {
        return audio_station_url;
    }

    public void setAudio_station_url(String audio_station_url) {
        this.audio_station_url = audio_station_url;
    }
}
